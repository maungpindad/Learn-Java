public class Java04_BooleanDataType {
    public static void main(String[] args) {
        // Declare and initialize boolean variables

        boolean isJavaFun = true; // A boolean variable that indicates if Java is fun
        boolean isFishTasty = false; // A boolean variable that indicates if fish is tasty

        // Print the values to the console
        System.out.println("Is Java fun? " + isJavaFun);
        System.out.println("Is fish tasty? " + isFishTasty);
    }
}
