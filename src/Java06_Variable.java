public class Java06_Variable {
    public static void main(String[] args) {
        /*
         * A variable in Java is a container that holds a value. It has a name, a data
         * type, and a value. Variables are used to store and manipulate data in a
         * program. Here are some examples of how to declare and use variables in Java:
         */

        // Declaring and initializing an integer variable
        int age = 25;
        System.out.println("Age: " + age);

        // Declaring and initializing a double variable
        double price = 19.99;
        System.out.println("Price: " + price);

        // Declaring and initializing a boolean variable
        boolean isJavaFun = true;
        System.out.println("Is Java fun? " + isJavaFun);

        // Declaring and initializing a char variable
        char grade = 'A';
        System.out.println("Grade: " + grade);

        // Declaring and initializing a String variable
        String name = "Alice";
        System.out.println("Name: " + name);
    }
}
